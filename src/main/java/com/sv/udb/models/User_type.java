package com.sv.udb.models;

/**
 *
 * @author kevin
 */
public class User_type {
    int id;
    String name;

    public User_type() {
    }

    public User_type(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return name;
    }
}
